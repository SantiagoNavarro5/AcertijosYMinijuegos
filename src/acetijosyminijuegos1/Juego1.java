    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String[] respuestasCorrectas = {"caballo", "ajolote", "ornitorrinco", "medusa"};
        String[] respuestasUsuario = {
            jTextField1.getText().trim().toLowerCase(),
            jTextField2.getText().trim().toLowerCase(),
            jTextField3.getText().trim().toLowerCase(),
            jTextField4.getText().trim().toLowerCase()
            //textField.getText() es para obtener la informaci√≥n 
            //.trim elimina los espacios en blanco innecesarios 
            //.toLowerCase() convierte todo a minusculas
        };
        StringBuilder mensajeError = new StringBuilder();
        boolean todasCorrectas = true;

        for (int i = 0; i < respuestasCorrectas.length; i++) {//Revisa todas las respuestas correctas
            if (!respuestasUsuario[i].equals(respuestasCorrectas[i])) {//Por cada respuesta que no coinicda pone el mensaje de error
                mensajeError.append("Respuesta " + (i + 1) + " incorrecta. \n");
                todasCorrectas = false;//Esto hace que cuando sean correctas salga correcto o incorrecto en el siguiente if
            }
        }

        // üìå Mostrar mensaje seg√∫n el resultado
        if (todasCorrectas) {
            Component panel = null;
            JOptionPane.showMessageDialog(panel, "¬°Correcto! La primera letra es: L.", "Resultado", JOptionPane.INFORMATION_MESSAGE);
        } else {
            Component panel = null;
            JOptionPane.showMessageDialog(panel, mensajeError.toString(), "Error", JOptionPane.ERROR_MESSAGE);
            //En el mensake llama al mensajeError creado anteriormente
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
